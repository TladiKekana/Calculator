using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculator.Core
{
    internal class MyCalculator
    {
        static public double Addition(double a, double b)
        {
            return a + b;
        }

        static public double Subtration(double a, double b)
        {
            return a - b;
        }

        static public double Multiply(double a, double b)
        {
            return a * b;
        }

        static public double Divide(double a, double b)
        {
            return a / b;
        }
    }
}
